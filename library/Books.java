package library;

public class Books {

	String  author;
	String title;
	Genre genre;
	
		
	public Books () {
			
	}


	public String getAuthor() {
		return author;
	}


	public void setAuthor(String author) {
		this.author = author;
	}


	public String getTitle() {
		return title;
	}


	public void setTitle(String namebook) {
		this.title = namebook;
	}


	public Genre getGenre() {
		return genre;
	}


	public void setGenre(Genre genre) {
		this.genre = genre;
	}
	

	
}
